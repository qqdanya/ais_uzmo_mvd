class Command(BaseCommand):
    help = "Создает начальные отделы, территориальные органы и суперпользователя из env."

    def handle(self, *args, **options):
        for order, (slug, name) in enumerate(DEPARTMENTS, start=1):
            Department.objects.update_or_create(slug=slug, defaults={"name": name, "order_number": order, "is_active": True})

        for number, name, children in ORGANS:
            parent, _ = TerritorialOrgan.objects.update_or_create(
                name=name,
                defaults={"order_number": Decimal(number), "is_active": True},
            )
            for idx, child_name in enumerate(children, start=1):
                TerritorialOrgan.objects.update_or_create(
                    name=child_name,
                    defaults={"order_number": Decimal(f"{number}.{idx:02d}"), "parent": parent, "is_active": True},
                )

        username = os.getenv("SUPERUSER_USERNAME")
        password = os.getenv("SUPERUSER_PASSWORD")
        email = os.getenv("SUPERUSER_EMAIL", "")
        if username and password:
            User = get_user_model()
            user, created = User.objects.get_or_create(username=username, defaults={"email": email, "is_staff": True, "is_superuser": True})
            if created:
                user.set_password(password)
                user.save(update_fields=["password"])
            UserProfile.objects.get_or_create(user=user, defaults={"role": UserProfile.Role.ADMIN})
            self.stdout.write(self.style.SUCCESS(f"Суперпользователь готов: {username}"))

        self.stdout.write(self.style.SUCCESS("Начальные данные загружены без дублей."))
